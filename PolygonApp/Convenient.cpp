/***********************************************************
|  関数：sign()
|  説明：float型の符号を返す関数
|  引数：float i       
|  戻値：float            -1:iが負の数 1:iが正の数
***********************************************************/
float sign(float i) {			/* 符号を返す */
    if (i < 0) {
        return (float)-1;
    }
    else {
        return (float)1;
    }

}